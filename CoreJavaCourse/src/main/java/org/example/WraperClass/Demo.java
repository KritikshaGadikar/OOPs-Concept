package org.example.WraperClass;

public class Demo {
    public static void main(String args[]) {

         int num = 7;

         Integer a = num;                 // data type to class  called as auto boxing

         int b = a;                    // class to data type called as auto unboxing

        System.out.println(a);
        System.out.println(b);
    }
}
